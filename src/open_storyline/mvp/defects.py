def _domain(code: str) -> DefectDomain:
    if code.startswith("ACTIVITY_"):
        return DefectDomain.ACTIVITY
    if code in {
        "ARTIFACT_PATH_INVALID",
        "SESSION_SOURCE_PATH_INVALID",
        "UPLOAD_PATH_INVALID",
    }:
        return DefectDomain.SECURITY
    if code.startswith(("AUTH_", "CSRF_", "REQUEST_ORIGIN", "UNAUTHENTICATED", "INVALID_CREDENTIALS", "LOGIN_")):
        return DefectDomain.SECURITY
    if code.startswith(("SESSION_SOURCE", "SESSION_MEDIA", "SOURCE_", "UPLOAD_", "VIDEO_TYPE", "MEDIA_HAS_NO_AUDIO", "AUDIO_EXTRACTION", "STT_INPUT")):
        return DefectDomain.SOURCE
    if code.startswith(("SESSION_", "JOB_", "PROMPT_", "ARTIFACT_")):
        return DefectDomain.WORKSPACE
    if code.startswith(("NINEROUTER_", "MISTRAL_", "STT_", "IMAGE_", "PEXELS_", "REMOTE_IMAGE")):
        return DefectDomain.PROVIDER
    if code.startswith("CATALOG_"):
        return DefectDomain.CATALOG
    if code.startswith(("ASSET_", "STOCK_", "GENERATED_ASSET", "CREATIVE_INTENT")):
        return DefectDomain.ASSET
    if code.startswith(("CAPTION_", "SUBTITLE_")):
        return DefectDomain.CAPTION
    if code.startswith(("VISUAL_", "CROP_VISUAL", "FRAME_", "SCENE_")):
        return DefectDomain.EVIDENCE
    if code.startswith(("EDIT_", "CLIP_", "SEGMENT_", "OVERLAY_", "REGION_", "TRACK_", "CAPABILITY_", "TRANSITION_", "SHORTS_", "NO_VALID_SHORTS", "FULL_FRAME", "EVIDENCE_", "FIRST_SEGMENT", "REPAIR_")):
        return DefectDomain.PLANNING
    if code.startswith(("RENDER_", "FILTER", "COMPOSITION_", "FFMPEGA_", "VIDEO_RENDER", "MEDIA_PROBE", "FFPROBE", "EFFECT_", "EXTERNAL_ASSET_OMITTED")):
        return DefectDomain.RENDER
    if code.startswith(("QA_", "ACTIVE_PICTURE", "REFERENCE_QUALITY", "BLACK_", "FROZEN_", "LONG_", "ATTENTION_", "INACTIVE_", "SEMANTIC_", "RHYTHM_", "PLANNED_", "REQUESTED_", "UNREQUESTED_", "UNPLANNED_", "UNEXPLAINED_", "SEVERE_", "AUDIO_", "OUTPUT_", "VIDEO_CODEC", "DURATION_", "CREATIVE_")):
        return DefectDomain.OUTCOME
    if code.startswith(("DATABASE_", "CHECKPOINT_", "AUDIT_", "RETENTION_")):
        return DefectDomain.INFRASTRUCTURE
    return DefectDomain.OUTCOME


def _repair_strategy(code: str) -> RepairStrategy:
    if code in _VISUAL_REPAIR_CODES:
        return RepairStrategy.LLM_VISUAL_REPAIR
    if code in _PLAN_REPAIR_CODES:
        return RepairStrategy.LLM_PLAN_REPAIR
    if code in _CONDITIONAL_REPAIR_CODES:
        return RepairStrategy.CONDITIONAL_LLM_OR_FALLBACK
    if code in _POST_RENDER_FALLBACK_CODES:
        return RepairStrategy.DETERMINISTIC_FALLBACK
    if code in _FALLBACK_CODES:
        return RepairStrategy.DETERMINISTIC_FALLBACK
    if code in _PROVIDER_RETRY_CODES:
        return RepairStrategy.PROVIDER_RETRY
    if code in _TECHNICAL_CODES:
        return RepairStrategy.RENDERER_RETRY_OR_FAIL
    if code in _ADVISORY_CODES:
        return RepairStrategy.ADVISORY
    return RepairStrategy.TERMINAL


def _repair_phase(code: str, strategy: RepairStrategy) -> RepairPhase:
    if code in _PREDICTIVE_METADATA:
        return RepairPhase.PRE_RENDER
    if code in _POST_RENDER_FALLBACK_CODES:
        return RepairPhase.POST_RENDER
    return {
        RepairStrategy.LLM_VISUAL_REPAIR: RepairPhase.VISUAL_UNDERSTANDING,
        RepairStrategy.LLM_PLAN_REPAIR: RepairPhase.PRE_RENDER,
        RepairStrategy.CONDITIONAL_LLM_OR_FALLBACK: RepairPhase.PRE_RENDER,
        RepairStrategy.DETERMINISTIC_FALLBACK: RepairPhase.COMPILE,
        RepairStrategy.PROVIDER_RETRY: RepairPhase.TRANSPORT,
        RepairStrategy.RENDERER_RETRY_OR_FAIL: RepairPhase.POST_RENDER,
        RepairStrategy.ADVISORY: RepairPhase.POST_RENDER,
        RepairStrategy.TERMINAL: RepairPhase.NONE,
    }[strategy]


def _promotion_class(code: str, strategy: RepairStrategy) -> PromotionClass:
    if code in _TECHNICAL_CODES:
        return PromotionClass.TECHNICAL_BLOCKER
    if strategy is RepairStrategy.ADVISORY:
        return PromotionClass.ADVISORY
    if strategy in {
        RepairStrategy.LLM_PLAN_REPAIR,
        RepairStrategy.CONDITIONAL_LLM_OR_FALLBACK,
        RepairStrategy.DETERMINISTIC_FALLBACK,
    }:
        return PromotionClass.CREATIVE_LIMITATION
    return PromotionClass.TERMINAL


def _severity(promotion_class: PromotionClass, retryable: bool) -> DefectSeverity:
    if promotion_class is PromotionClass.TECHNICAL_BLOCKER:
        return DefectSeverity.TECHNICAL_BLOCKER
    if promotion_class is PromotionClass.CREATIVE_LIMITATION:
        return DefectSeverity.LIMITATION
    if promotion_class is PromotionClass.ADVISORY:
        return DefectSeverity.ADVISORY
    return DefectSeverity.RETRYABLE if retryable else DefectSeverity.TERMINAL


def _fallback(code: str) -> str | None:
    if code == "PREDICTIVE_SUBTITLE_SAFE_ZONE_CONFLICT":
        return "CAPTION_SAFE_ZONE_FALLBACK"
    if code == "PREDICTIVE_ACTIVE_PICTURE_RISK":
        return "VISUAL_REFRAME_FALLBACK"
    if code.startswith("PREDICTIVE_OVERLAY_"):
        return "RENDER_PREFLIGHT_FALLBACK"
    if code.startswith("VISUAL_"):
        return "VISUAL_REFRAME_FALLBACK"
    if code.startswith("CROP_VISUAL") or code in {"ACTIVE_PICTURE_TOO_SMALL"}:
        return "VISUAL_REFRAME_FALLBACK"
    if code in {
        "COMPOSITION_CROP_TARGET_TOO_WIDE",
        "COMPOSITION_LAYOUT_UNSUPPORTED",
    }:
        return "VISUAL_REFRAME_FALLBACK"
    if code.startswith("CAPTION_") or code.startswith("SUBTITLE_"):
        return "CAPTION_SAFE_ZONE_FALLBACK"
    if code in {
        "EDIT_PLAN_REGION_UNKNOWN",
        "EDIT_PLAN_REGION_OUTSIDE_CLIP",
        "EDIT_PLAN_TRACK_UNKNOWN",
        "EDIT_PLAN_TRACK_OUTSIDE_CLIP",
        "EDIT_PLAN_EVIDENCE_UNKNOWN",
        "REGION_REFERENCE_UNKNOWN",
        "REGION_REFERENCE_OUTSIDE_CLIP",
        "TRACK_REFERENCE_UNKNOWN",
        "TRACK_REFERENCE_OUTSIDE_CLIP",
        "EVIDENCE_REFERENCE_UNKNOWN",
        "FULL_FRAME_FALLBACK_UNAPPROVED",
    }:
        return "VISUAL_REFRAME_FALLBACK"
    if code.startswith(("ASSET_OVERLAY_", "ASSET_VISIBILITY_")):
        return "EXTERNAL_ASSET_OMITTED"
    if code in {
        "EDIT_PLAN_ASSET_BUDGET_EXCEEDED",
        "EDIT_PLAN_GENERATED_ASSET_BUDGET_EXCEEDED",
        "EDIT_PLAN_STOCK_ASSET_BUDGET_EXCEEDED",
        "ASSET_BUDGET_EXCEEDED",
        "REQUESTED_ASSETS_MISSING",
        "UNREQUESTED_ASSETS_USED",
    }:
        return "EXTERNAL_ASSET_OMITTED"
    if "TRANSITION" in code:
        return "TRANSITION_FALLBACK"
    if "CAPABILITY" in code or code == "EDIT_PLAN_OVERLAY_BUDGET_EXCEEDED":
        return "EFFECT_OMITTED"
    if code.startswith("FFMPEGA_") or code == "EFFECT_PLANNING_FAILED":
        return "EFFECT_OMITTED"
    if code in _PLAN_REPAIR_CODES or code in _CONDITIONAL_REPAIR_CODES:
        return "RENDER_PREFLIGHT_FALLBACK"
    return None


def _evidence(strategy: RepairStrategy) -> tuple[str, ...]:
    return {
        RepairStrategy.LLM_VISUAL_REPAIR: ("visual_evidence",),
        RepairStrategy.LLM_PLAN_REPAIR: ("edit_plan", "deterministic_validation"),
        RepairStrategy.CONDITIONAL_LLM_OR_FALLBACK: ("creative_intent", "capabilities"),
        RepairStrategy.DETERMINISTIC_FALLBACK: ("fallback_ledger",),
        RepairStrategy.PROVIDER_RETRY: ("provider_attempt",),
        RepairStrategy.RENDERER_RETRY_OR_FAIL: ("technical_qa",),
        RepairStrategy.ADVISORY: ("qa_finding",),
        RepairStrategy.TERMINAL: (),
    }[strategy]


def _predictive_policy(code: str) -> tuple[str | None, str | None, bool]:
    metadata = _PREDICTIVE_METADATA.get(code)
    if metadata is None:
        return None, None, False
    detector, _evidence_types, threshold, trigger_eligible = metadata
    return detector, threshold, trigger_eligible


def _presentation(
    code: str,
    domain: DefectDomain,
    promotion_class: PromotionClass,
) -> tuple[str, str, str, str]:
    override = _PRESENTATION_OVERRIDES.get(code)
    if override is not None:
        return override
    title_en = {
        PromotionClass.ADVISORY: "Creative review recommended",
        PromotionClass.CREATIVE_LIMITATION: "Creative limitation",
        PromotionClass.TECHNICAL_BLOCKER: "Technical blocker",
        PromotionClass.TERMINAL: "Processing issue",
    }[promotion_class]
    title_es = {
        PromotionClass.ADVISORY: "Revision creativa recomendada",
        PromotionClass.CREATIVE_LIMITATION: "Limitacion creativa",
        PromotionClass.TECHNICAL_BLOCKER: "Bloqueo tecnico",
        PromotionClass.TERMINAL: "Incidencia de procesamiento",
    }[promotion_class]
    domain_en, domain_es = _DOMAIN_LABELS[domain]
    readable = code.replace("_", " ").lower()
    return (
        title_en,
        f"The {domain_en} stage reported {readable}.",
        title_es,
        f"La etapa de {domain_es} registro el codigo {code}.",
    )


def _definition(code: str) -> DefectDefinition:
    strategy = _repair_strategy(code)
    promotion_class = _promotion_class(code, strategy)
    retryable = (
        code in _LEGACY_RETRYABLE_CODES
        or strategy in {
            RepairStrategy.ADVISORY,
            RepairStrategy.LLM_VISUAL_REPAIR,
            RepairStrategy.LLM_PLAN_REPAIR,
            RepairStrategy.CONDITIONAL_LLM_OR_FALLBACK,
            RepairStrategy.DETERMINISTIC_FALLBACK,
            RepairStrategy.PROVIDER_RETRY,
            RepairStrategy.RENDERER_RETRY_OR_FAIL,
        }
        or code.endswith(("_TIMEOUT", "_RATE_LIMITED", "_UNAVAILABLE"))
    )
    domain = _domain(code)
    title_en, description_en, title_es, description_es = _presentation(
        code,
        domain,
        promotion_class,
    )
    detector, threshold, trigger_eligible = _predictive_policy(code)
    evidence_requirements = (
        _PREDICTIVE_METADATA[code][1]
        if code in _PREDICTIVE_METADATA
        else _evidence(strategy)
    )
    return DefectDefinition(
        code=code,
        domain=domain,
        default_severity=_severity(promotion_class, retryable),
        visibility=(
            DefectVisibility.PUBLIC if code in _PUBLIC_CODES else DefectVisibility.AUDIT
        ),
        repair_strategy=strategy,
        repair_phase=_repair_phase(code, strategy),
        evidence_requirements=evidence_requirements,
        safe_fallback_code=_fallback(code),
        retryable=retryable,
        retry_action=(RetryAction.RETRY_DEFECTS if retryable else RetryAction.NONE),
        promotion_class=promotion_class,
        title_en=title_en,
        description_en=description_en,
        title_es=title_es,
        description_es=description_es,
        legacy_aliases=(code.lower(),) if code in _PUBLIC_CODES else (),
        detector=detector,
        threshold=threshold,
        trigger_eligible=trigger_eligible,
    )


def _build_registry(codes: Iterable[str]) -> tuple[dict[str, DefectDefinition], dict[str, str]]:
    registry: dict[str, DefectDefinition] = {}
    aliases: dict[str, str] = {}
    for code in sorted(codes):
        if not _SAFE_CODE.fullmatch(code) or code in registry:
            raise RuntimeError(f"invalid or duplicate defect code: {code}")
        definition = _definition(code)
        registry[code] = definition
        for alias in definition.legacy_aliases:
            key = alias.strip().upper()
            owner = aliases.get(key)
            if owner is not None and owner != code:
                raise RuntimeError(f"duplicate defect alias: {alias}")
            aliases[key] = code
    return registry, aliases


_POLICY_CODES = (
    _TECHNICAL_CODES
    | _ADVISORY_CODES
    | _VISUAL_REPAIR_CODES
    | _PLAN_REPAIR_CODES
    | _CONDITIONAL_REPAIR_CODES
    | _FALLBACK_CODES
    | _POST_RENDER_FALLBACK_CODES
    | _PROVIDER_RETRY_CODES
    | _LEGACY_RETRYABLE_CODES
    | _PUBLIC_CODES
)
_registry, _aliases = _build_registry(_REGISTERED_CODES | _POLICY_CODES)
DEFECT_REGISTRY = MappingProxyType(_registry)
DEFECT_ALIASES = MappingProxyType(_aliases)
DEFECT_REGISTRY_SHA256 = sha256(json.dumps(
    {
        code: asdict(definition)
        for code, definition in sorted(DEFECT_REGISTRY.items())
    },
    ensure_ascii=True,
    sort_keys=True,
    separators=(",", ":"),
).encode("utf-8")).hexdigest()

UNKNOWN_DEFECT = DefectDefinition(
    code="UNKNOWN_DEFECT",
    domain=DefectDomain.UNKNOWN,
    default_severity=DefectSeverity.TERMINAL,
    visibility=DefectVisibility.PUBLIC,
    repair_strategy=RepairStrategy.TERMINAL,
    repair_phase=RepairPhase.NONE,
    evidence_requirements=(),
    safe_fallback_code=None,
    retryable=False,
    retry_action=RetryAction.NONE,
    promotion_class=PromotionClass.TECHNICAL_BLOCKER,
    title_en="Unknown processing issue",
    description_en="An unregistered defect code was reported and failed closed.",
    title_es="Incidencia de procesamiento desconocida",
    description_es="Se registro un codigo no reconocido y el proceso fallo de forma segura.",
    detector=None,
    threshold=None,
    trigger_eligible=False,
)


def normalize_defect_code(code: Any) -> str:
    normalized = str(code or "").strip().upper()[:120]
    if not _SAFE_CODE.fullmatch(normalized):
        return "UNKNOWN_DEFECT"
    return DEFECT_ALIASES.get(normalized, normalized)


def defect_definition(code: Any) -> DefectDefinition:
    return DEFECT_REGISTRY.get(normalize_defect_code(code), UNKNOWN_DEFECT)


def defect_public_metadata(code: Any) -> dict[str, Any]:
    candidate = str(code or "").strip().upper()[:120]
    raw_code = candidate if _SAFE_CODE.fullmatch(candidate) else "UNKNOWN_DEFECT"
    normalized = normalize_defect_code(raw_code)
    definition = DEFECT_REGISTRY.get(normalized, UNKNOWN_DEFECT)
    return {
        "registry_version": DEFECT_REGISTRY_VERSION,
        "registered": definition is not UNKNOWN_DEFECT,
        "raw_code": raw_code,
        "canonical_code": definition.code,
        "domain": definition.domain.value,
        "severity": definition.default_severity.value,
        "repair_strategy": definition.repair_strategy.value,
        "repair_phase": definition.repair_phase.value,
        "retryable": definition.retryable,
        "retry_action": definition.retry_action.value,
        "promotion_class": definition.promotion_class.value,
        "detector": definition.detector,
        "threshold": definition.threshold,
        "trigger_eligible": definition.trigger_eligible,
        "en": {"title": definition.title_en, "description": definition.description_en},
        "es": {"title": definition.title_es, "description": definition.description_es},
    }


def retryable_for_code(code: Any, *, legacy_suffix_compatibility: bool = True) -> bool:
    normalized = normalize_defect_code(code)
    definition = DEFECT_REGISTRY.get(normalized)
    if definition is not None:
        return definition.retryable
    return bool(
        legacy_suffix_compatibility
        and normalized.endswith(("_TIMEOUT", "_RATE_LIMITED", "_UNAVAILABLE"))
    )


def promotion_class_for_code(code: Any) -> PromotionClass:
    return defect_definition(code).promotion_class
