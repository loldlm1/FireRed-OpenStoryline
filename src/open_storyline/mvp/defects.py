@dataclass(frozen=True)
class DefectDefinition:
    code: str
    domain: DefectDomain
    default_severity: DefectSeverity
    visibility: DefectVisibility
    repair_strategy: RepairStrategy
    repair_phase: RepairPhase
    evidence_requirements: tuple[str, ...]
    safe_fallback_code: str | None
    retryable: bool
